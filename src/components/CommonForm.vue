<template>
     <el-form ref="form" label-width="100px" :model="form" :inline="inline" >
        <el-form-item v-for="item in formLabel" :key="item.label" :label="item.label">
            <!-- 文本输入组件 -->
            <el-input
                v-if="item.type === 'input'"
                :placeholder="'请输入' + item.label"
                v-model="form[item.model]"
            ></el-input>
            <el-switch v-if="item.type === 'switch'" v-model="form[item.model]"></el-switch>
            <!-- 日期组件 -->
            <el-date-picker
                v-if="item.type === 'date'"
                type="date"
                value-format="yyyy-MM-dd"
                placeholder="选择日期"
                v-model="form[item.model]"
            ></el-date-picker>
            <!-- 下拉组件 -->
            <el-select
                v-if="item.type === 'select'"
                placeholder="请选择"
                v-model="form[item.model]"
            >
                <el-option
                    v-for="item in item.opts"
                    :key="item.value"
                    :label="item.label"
                    :value="item.value"
                ></el-option>
            </el-select>
        </el-form-item>
        <!-- 自定义组件 -->
        <el-form-item><slot></slot></el-form-item>
    </el-form>
</template>

<script>

export default {
    name: 'CommonForm',
    props: {
        // form 的相关配置
        formLabel: Array,
        // form 的数据，进行双向绑定
        form: Object,
        // 行内表单模式 
        inline: Boolean
    },
    data() {
        return {}
    }
}
</script>